# 单行注释解释
print("1. 单行注释：以 '#' 开头，用于对代码行或小部分代码进行简要说明。Python 解释器会忽略 '#' 及其后面的内容。示例如下：")
# 这是一个单行注释，下面代码用于打印问候语
print("Hello, Python!")

# 多行注释（使用三单引号）解释
print("\n2. 多行注释（使用三单引号）：使用三单引号 ''' 包裹，可以跨越多行。常用于对代码块、函数、类等进行详细说明。示例如下：")
'''
这是一个使用三单引号的多行注释
可以在这里详细描述代码的功能、输入输出等信息
下面的代码会打印数字 1 到 5
'''
for i in range(1, 6):
    print(i)

# 多行注释（使用三双引号）解释
print("\n3. 多行注释（使用三双引号）：使用三双引号 \"\"\" 包裹，同样可以跨越多行，用途和三单引号多行注释一样。示例如下：")
"""
这是一个使用三双引号的多行注释
可以对后续代码进行详细的功能说明
下面代码会打印简单的数学运算结果
"""
result = 2 + 3
print(result)
    