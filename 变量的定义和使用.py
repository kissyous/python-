# 定义整数类型变量
age = 25
print("年龄:", age)

# 定义浮点数类型变量
height = 1.75
print("身高:", height)

# 定义字符串类型变量
name = "张三"
print("姓名:", name)

# 定义布尔类型变量
is_student = True
print("是否是学生:", is_student)

# 变量的重新赋值
age = 26
print("更新后的年龄:", age)

# 变量之间的运算
new_age = age + 5
print("五年后的年龄:", new_age)

#以上是基本数据类型